import mongoose from 'mongoose';

const MONGODB_URI = process.env.MONGODB_URI;

// Test mode: Allow running without MongoDB for UI testing
const TEST_MODE = !MONGODB_URI || MONGODB_URI.trim() === '';

if (TEST_MODE) {
  console.warn('⚠️  Running in TEST MODE - No database connection. Using mock data.');
}

/**
 * Global is used here to maintain a cached connection across hot reloads
 * in development. This prevents connections growing exponentially
 * during API Route usage.
 */
interface MongooseCache {
  conn: typeof mongoose | null;
  promise: Promise<typeof mongoose> | null;
}

declare global {
  var mongoose: MongooseCache;
}

let cached = global.mongoose;

if (!cached) {
  cached = global.mongoose = { conn: null, promise: null };
}

async function dbConnect() {
  // In test mode, return a mock connection
  if (TEST_MODE) {
    return mongoose as typeof mongoose;
  }

  if (cached.conn) {
    return cached.conn;
  }

  if (!cached.promise) {
    const opts = {
      bufferCommands: false,
    };

    cached.promise = mongoose.connect(MONGODB_URI!, opts).then((mongoose) => {
      return mongoose;
    });
  }
  try {
    cached.conn = await cached.promise;
  } catch (e) {
    cached.promise = null;
    throw e;
  }

  return cached.conn;
}

export default dbConnect;
